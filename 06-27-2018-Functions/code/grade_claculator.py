grade = 80

if grade >= 90 and grade <= 100:
    print("You got a A")
elif grade >= 80 and grade <= 89:
    print("You got a B")
elif grade >= 70 and grade <= 79:
    print("You got a C")
elif grade >= 60 and grade <= 69:
    print("You got a D")
else:
    print("I'm sorry to say you failed this class")
